package com.example.lostandfound;

public class LostFound {
    private String foundEmail;
    private String lostEmail;
    private String detail;
    private String foundDate;

    public String getFoundEmail() {
        return foundEmail;
    }

    public void setFoundEmail(String foundEmail) {
        this.foundEmail = foundEmail;
    }

    public String getLostEmail() {
        return lostEmail;
    }

    public void setLostEmail(String lostEmail) {
        this.lostEmail = lostEmail;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getFoundDate() {
        return foundDate;
    }

    public void setFoundDate(String foundDate) {
        this.foundDate = foundDate;
    }
}
