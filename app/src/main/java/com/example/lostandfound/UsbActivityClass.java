package com.example.lostandfound;

public class UsbActivityClass {

    private String type;
    private String lostUsbType;
    private String companyName;
    private String colour;
    private String dataCapcity;
    private String date;
    private String place;
    private String labNo;
    private String email;
    private String check;

    public UsbActivityClass() {
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getLostUsbType() {
        return lostUsbType;
    }

    public void setLostUsbType(String lostUsbType) {
        this.lostUsbType = lostUsbType;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public String getDataCapcity() {
        return dataCapcity;
    }

    public void setDataCapcity(String dataCapcity) {
        this.dataCapcity = dataCapcity;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getLabNo() {
        return labNo;
    }

    public void setLabNo(String labNo) {
        this.labNo = labNo;
    }

    public String getCheck() {
        return check;
    }

    public void setCheck(String check) {
        this.check = check;
    }
}
